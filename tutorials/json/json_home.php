<p><h4>Mastering JSON is paramount</h4></p>
<p>What is JSON?</p>
<ol>
	<li>
		JSON staat voor: JavaScript Object Notation.
	</li>
	<li>
		JSON is a syntax for storing and exchanging data.
	</li>
	<li>
		JSON is an easier-to-use alternative to XML.
	</li>
	<li>JSON is a lightweight data-interchange format</li>
	<li>JSON is language independent *</li>
	<li>JSON is "self-describing" and easy to understand</li>
	<li>Note	* JSON uses JavaScript syntax, but the JSON format is text only, just like XML.
Text can be read and used as a data format by any programming language.</li>
</ol>





